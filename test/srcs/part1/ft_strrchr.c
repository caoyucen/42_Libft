#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*a;

	a = (char*)s;
	i = -1;
	if (a != NULL)
		i = ft_strlen(a) - 1;
	while (i >= 0)
	{
		if (s[i] == (char)c)
			return ((char*)s + i);
	}
	return (0);
}
